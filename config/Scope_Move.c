    FPGA_WriteBuffer((UINT16*)anBuffer, nAddr, 2);
    // --->2. EN   ->lock
	anBuffer[0] = (UINT16)( FPGA_WR_V_MOTOR_LOCK & 0xFFFF);
	anBuffer[1] = (UINT16)((FPGA_WR_V_MOTOR_LOCK >> 16) & 0xFFFF);
	// address
